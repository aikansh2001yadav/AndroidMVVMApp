package com.example.androidmvvmapp.data.model

/**
 * Data class that contains information of the user
 */
data class User(
    /**
     * Stores id of the user
     */
    val id: String? = null,
    /**
     * Stores name of the user
     */
    val name: String? = null,
    /**
     * Stores last name of the user
     */
    val lastName: String? = null,
    /**
     * Stores email of the user
     */
    val email: String? = null
)